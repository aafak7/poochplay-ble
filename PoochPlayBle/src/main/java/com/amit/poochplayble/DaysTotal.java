package com.amit.poochplayble;


public class DaysTotal {
    public int utc;
    public int totalStpe;
    public int totalKcl;
    public String time;

    public DaysTotal() {
    }

    public int getUtc() {
        return this.utc;
    }

    public void setUtc(int utc) {
        this.utc = utc;
    }

    public int getTotalStpe() {
        return this.totalStpe;
    }

    public void setTotalStpe(int totalStpe) {
        this.totalStpe = totalStpe;
    }

    public int getTotalKcl() {
        return this.totalKcl;
    }

    public void setTotalKcl(int totalKcl) {
        this.totalKcl = totalKcl;
    }

    public String getTime() {
        return this.time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
